# -*- coding: utf-8 -*-
# Ejemplo de tipos de datos en Python

# Int
age = 30
print("Edad:", age)

# Float
height = 1.75
print("Altura:", height)

# String
name = "Ana"
print("Nombre:", name)

# Boolean
is_student = False
print("Es estudiante?:", is_student)

# None, ausencia de valor
address = None
print("Direccion:", address)
