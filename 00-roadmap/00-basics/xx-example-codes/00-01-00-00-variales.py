# -*- coding: utf-8 -*-
# Variables en Python

# Declaracion e inicializacion de variables
edad = 28
altura = 1.75
pais = "Chile"

# Acceder a las variables
print("Valores iniciales:")
print("Edad:", edad)
print("Altura:", altura)
print("Pais:", pais)

# Modificar el valor de una variable
edad = 30
altura = 1.90

print("Valores modificados:")
print("Edad:", edad)
print("Altura:", altura)

# En Python el tipo es dinamico
edad = "Treinta"
print("Edad:", edad)
